public class SomeMath {
    public static void main(String[] args) {
        int smallInt = 1;
        double smallDouble = 1.0;

        int bigInt = 3;
        double bigDouble = 3.0;

        System.out.println("Int things: " + (smallInt / bigInt));
        System.out.println("Double things: " + (smallDouble / bigDouble));
    }
}
