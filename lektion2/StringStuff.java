public class StringStuff {
    public static void main(String[] args) {
        int myVar = 5;
        System.out.println("Värdet är: " + myVar);
        System.out.println("Värdet är: " + myVar + 2);
        System.out.println("Värdet är: " + (myVar + 2));

        int result = myVar + 2;
        System.out.println("Värdet är: " + result);
    }
}
