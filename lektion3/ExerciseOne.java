public class ExerciseOne {
    public static void main(String[] args) {
        byte theByte = 100;
        short theShort = 20_000;
        int theInt = 2_000_000_000;
        long theLong = 123_456_789_000L;

        System.out.println(theByte);
        System.out.println(theShort);
        System.out.println(theInt);
        System.out.println(theLong);

        float theFloat = 123.34f;
        double theDouble = 123.34;

        System.out.println(theFloat);
        System.out.println(theDouble);

        char theChar = 'A';

        System.out.println(theChar);

        boolean theBool = false;

        System.out.println(theBool);
    }
}
